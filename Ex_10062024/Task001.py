# 1.Explain the difference between the = operator and the == operator in Python.
# The = operator is used to assign a value to a variable, while the == operator is used to compare two values.

# 2.What does the ** operator do in Python, and how is it used?
# The ** operator is used to raise a number to a power. It is used like this:
print(2 ** 3)  # Prints 8
print(5 ** 2)  # Prints 25

# 3.What does the ^ operator do in Python, and in what context is it commonly used?
# The ^ operator is used to perform bitwise XOR (exclusive OR) on two numbers. It is used like this:
print(2 ^ 3)  # Prints 1
print(5 ^ 2)  # Prints 7
